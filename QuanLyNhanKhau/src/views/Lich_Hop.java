            String diadiem = txtdiadiem.getText();
           
            String query = "INSERT INTO `lich_hop` (`chuDe`,`suKien`, `thoiGian`, `ngayHop`, `diaDiem`) values (?,?,?,?,?)";
            Connection conn = MysqlConnection.getMysqlConnection();

