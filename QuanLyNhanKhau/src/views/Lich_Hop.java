     public void load(){
        try { 
            Connection conn =  MysqlConnection.getMysqlConnection();
            String sql = "SELECT `id`,`chuDe`,`suKien`,`thoiGian`,`ngayHop`, `diaDiem` FROM `lich_hop` ";
            PreparedStatement ps = conn.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();
            while (rs.next()) {
                String ID = String.valueOf(rs.getInt("id"));
                String chuDe= rs.getString("chuDe");
                String suKien= rs.getString("suKien");
                String thoiGian = rs.getString("thoiGian");
                String ngayHop = rs.getString("ngayHop");
                String diaDiem = rs.getString("diaDiem");
                String tbData[] = {ID, chuDe,suKien,thoiGian, ngayHop,diaDiem};
                DefaultTableModel tblModel = (DefaultTableModel)tbList1.getModel();
                tblModel.addRow(tbData);              
                
                
            tbList1.setModel(DbUtils.resultSetToTableModel(rs));
            }
        } catch (Exception e) {
        }
    }

