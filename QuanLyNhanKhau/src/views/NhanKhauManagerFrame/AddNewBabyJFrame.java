    // check cac gia tri duoc nhap vao form
    private boolean validateValueInForm1() {
        // check null
        if (hoTenTxb1.getText().trim().isEmpty() 
                || noiKhaiSinhTxb1.getText().trim().isEmpty()
                || tonGiaoTxb1.getText().trim().isEmpty()
                || danTocTxb1.getText().trim().isEmpty()
                || quocTichTxb1.getText().trim().isEmpty()
                || soCMTTxb1.getText().trim().isEmpty()
                || noiThuongTruTxb1.getText().trim().isEmpty()
                || diaChiHienNayTxb1.getText().trim().isEmpty()
                ){
            JOptionPane.showMessageDialog(rootPane, "Vui lòng nhập hết các trường bắt buộc", "Warning", JOptionPane.WARNING_MESSAGE);
            return false;
        }
        // check dinh dang so chung minh thu
        try {
                long d = Long.parseLong(soCMTTxb1.getText());
            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, "Số CMT không thể chứa các ký tự", "Warning", JOptionPane.WARNING_MESSAGE);
                return false;
            }
        // kiem tra do dai cmt
        if (soCMTTxb1.getText().length() != 9 && soCMTTxb1.getText().length() != 12) {
            JOptionPane.showMessageDialog(rootPane, "Vui lòng nhập đúng định dạng CMT", "Warning", JOptionPane.WARNING_MESSAGE);
            return false;
        }
        return true;
    }
    
