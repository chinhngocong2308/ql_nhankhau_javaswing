    private void CreateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CreateBtnActionPerformed
        if (validateValueInForm1()) {
            // tao moi 1 doi tuong nhan khau
            NhanKhauModel temp = this.nhanKhauBean.getNhanKhauModel();
            ChungMinhThuModel cmt = this.nhanKhauBean.getChungMinhThuModel();
            temp.setBietDanh(bietDanhTxb1.getText());
            temp.setHoTen(hoTenTxb1.getText());
            temp.setNamSinh(namSinhDateC1.getDate());
            temp.setGioiTinh(gioiTinhCbb1.getSelectedItem().toString());
            temp.setNoiKhaiSinh(noiKhaiSinhTxb1.getText());
            temp.setTonGiao(tonGiaoTxb1.getText());
            temp.setDanToc(danTocTxb1.getText());
            temp.setQuocTich(quocTichTxb1.getText());
            cmt.setSoCMT(soCMTTxb1.getText());
            temp.setNoiThuongTru(noiThuongTruTxb1.getText());
            temp.setDiaChiHienNay(diaChiHienNayTxb1.getText());
           
            temp.setIdNguoiTao(LoginController.currentUser.getID());
            try {
                if (this.controller.addNewPeople(this.nhanKhauBean)) {
                    JOptionPane.showMessageDialog(null, "Khai sinh thành công!!");
                    close();
                    parentController.refreshData();
                }
            } catch (Exception e) {
                System.out.println(e.getMessage());
                JOptionPane.showMessageDialog(rootPane, "Có lỗi xảy ra. Vui long kiểm tra lại!!", "Warning", JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_CreateBtnActionPerformed

